"""
Escribe un programa en Python que, usando un bucle while, cuente hacia atrás desde el 10 hasta el 0, 
éste último incluido. Y que luego imprima la palabra ``Despegamos!''. 
Recuerda, usa un bucle WHILE, no vayas a hacerlo con un bucle FOR.
"""

i = 10
while i > -1:
    print(i)
    i = i - 1
print ("Despegamos!")